package com.ujjawal.projects.Controller.Dto;

public class Details {

	public String name;
	public String phone;
	public String size;
	public String type;
	public int cake;
	public String date;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	

	public int getCake() {
		return cake;
	}
	public void setCake(int cake) {
		this.cake = cake;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
